void send_cmd(uint8_t data)
{
    I2C_StartTransmission(I2C1, I2C_Direction_Transmitter, 0x3c);
    I2C_WriteData(I2C1, 0x00);
    I2C_WriteData(I2C1, data);
    I2C_GenerateSTOP(I2C1, ENABLE);
}

void set_display_on(bool on)
{
    if (on)
        send_cmd(CMD_DISPLAY_ON);
    else
        send_cmd(CMD_DISPLAY_OFF);
}


void set_memory_addressing_mode(uint8_t mode)
{
    send_cmd(CMD_ADDRESSING_MODE);
    send_cmd(mode);
}

void set_clock(uint8_t clock)
{
    send_cmd(CMD_DISPLAY_CLOCK);
    send_cmd(clock);
}

void set_multiplex(uint8_t lcd_height)
{
    send_cmd(CMD_MULTIPLEX);
    send_cmd(lcd_height);
}

void set_display_offset(uint8_t offset)
{
    send_cmd(CMD_DISPLAY_OFFSET);
    send_cmd(offset);
}

void set_display_start_line(uint8_t start)
{
    send_cmd(start | 0x40);
}

void set_charge_bump(bool on)
{
    send_cmd(CMD_CHARGE_BUMP);
    if (on)
        send_cmd(0x14);
    else
        send_cmd(0x10);
}

void set_com_pin_config(uint8_t config)
{
    send_cmd(CMD_COM_PIN_CONFIG);
    send_cmd(config);
}

void set_contrast(uint8_t contrast)
{
    send_cmd(CMD_CONTRAST);
    send_cmd(contrast);
}

void set_precharge_period(uint8_t period)
{
    send_cmd(CMD_PRECHARGE_PERIOD);
    send_cmd(period);
}

void set_vcomh_deselect(uint8_t vcc)
{
    send_cmd(CMD_VCOMH_DESELECT);
    send_cmd(vcc);
}

void set_entire_display_on(bool enable)
{
    if (enable)
        send_cmd(CMD_ENTIRE_DISPLAY_ON_ENABLE);
    else
        send_cmd(CMD_ENTIRE_DISPLAY_ON_DISABLE);
}

void set_normal_display(bool normal)
{
    if (normal)
        send_cmd(CMD_NORMAL_DISPLAY);
    else
        send_cmd(CMD_INVERSE_DISPLAY);
}

void stop_scroll(void)
{
    send_cmd(CMD_STOP_SCROLL);
}

void set_column_address(uint8_t start, uint8_t end)
{
    send_cmd(CMD_COLUMN_ADDRESS);
    send_cmd(start);
    send_cmd(end);
}

void set_page_address(uint8_t start, uint8_t end)
{
    send_cmd(CMD_PAGE_ADDRESS);
    send_cmd(start);
    send_cmd(end);
}

void set_page_start_address(uint8_t start)
{
    send_cmd(0x0b | start);
}

void display(void)
{
    int i, x;

    for (i = 0; i < (LCD_WIDTH * LCD_HEIGHT / 8); i++) {
        I2C_StartTransmission(I2C1, I2C_Direction_Transmitter, 0x3c);
        I2C_WriteData(I2C1, 0x40);
        for (x = 0; x < 16; x++) {
            I2C_WriteData(I2C1, buffer[i]);
            i++;
        }
        i--;
        I2C_GenerateSTOP(I2C1, ENABLE);
    }
}

void main(void)
{
    init_I2C1();

    set_display_on(false);
    set_memory_addressing_mode(HORIZONTAL_ADDRESSING);
    set_clock(0x80);
    set_multiplex(64 - 1); //0 ~ 63
    set_display_offset(0);
    set_display_start_line(0);
    set_charge_bump(true);
    set_com_pin_config(0x12);
    set_contrast(0x8f);
    set_precharge_period(0x22);
    set_vcomh_deselect(0x40);
    set_entire_display_on(false);
    set_normal_display(true);
    stop_scroll();

    set_display_on(true);

    set_column_address(0, LCD_WIDTH - 1);
    set_page_address(0, 7);
    display();

    while (1);
}


